function getAllLogs() {
  return getStorageItem(STORAGE_KEY, initialAuditLogs);
}

export const auditLogService = {
  async fetchAuditLogs(params = {}) {
    await delay();

    const allLogs = getAllLogs();
    const filtered = filterLogs(allLogs, params.filters ?? {});
    const sorted = sortLogs(filtered, params.sortField, params.sortDirection);
    const paginated = paginateLogs(sorted, params.page ?? 1, params.pageSize ?? 10);
    const stats = computeStats(allLogs);

    const uniqueUsers = [...new Set(allLogs.map((log) => log.userName).filter(Boolean))].sort((a, b) =>
      a.localeCompare(b)
    );

    return {
      logs: paginated.logs,
      totalRecords: paginated.totalRecords,
      totalPages: paginated.totalPages,
      page: paginated.page,
      rangeStart: paginated.rangeStart,
      rangeEnd: paginated.rangeEnd,
      stats,
      allFilteredLogs: sorted,
      uniqueUsers,
    };
  },

  async fetchAuditLogById(auditId) {
    await delay(200);
    const allLogs = getAllLogs();
    const log = allLogs.find((item) => item.auditId === auditId);
    if (!log) {
      throw new Error(`Audit log ${auditId} not found`);
    }
    return log;
  },
};
